<h2><strong>Security check mandated</strong></h2>

<p>Head of the municipal economic department Jimmy Maming said <a href="https://www.philstar.com/nation/2018/06/05/1821808/transfer-boracay-ferry-trips-caticlan-tabon-normal-official-says">ferry trips have been transferred to Tabon Jetty Port</a> due to the strong waves caused by the southwest monsoon or &ldquo;habagat.&rdquo; He said this is an annual transportation protocol.</p>

<p>Upon arriving at the Tabon port, officials of the Municipality of Aklan designated separate lines for the security check on workers, Boracay residents and senior citizens as well as persons with disabilities.</p>

