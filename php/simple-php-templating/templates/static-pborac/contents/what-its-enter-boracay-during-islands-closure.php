<p>Each port is manned by members of the Philippine Coast Guard, Philippine National Police, Philippine Navy and municipal officers.</p>

<p>Identified tourists are stopped by the authorities who seek identification card for all residents and workers before they are permitted entry to the island. Residents are asked to present IDs with a specific residence address in any of the three villages.</p>

<p>The municipal officials recognize government IDs while non-government IDs are only accepted if it is accompanied by a barangay certification of residences.</p>

<p>The government&rsquo;s guidelines also disallow entry of visitors of Boracay residents except under emergency situations and with the clearance of the security committee composed of the representatives from the DILG, PNP and the local government.</p>

<p>Foreign residents also have to undergo checks and have to be revalidated by the Bureau of Immigration.</p>

<p>Meanwhile, members of the press covering the island closure will only be allowed if they are accredited by the DOT.</p>

<p>A DOT representative is present at the security checkpoint at the jetty port to certify the entrance of the media.</p>

<p>The members of the press are asked to login details of arrival and departure.</p>

<h2><strong>Port operation generally smooth</strong></h2>

<p>Despite the stiffer measures and interrogation process imposed on passengers going in and out of Boracay, Maming said&nbsp;operations at the ports have been so far smooth.</p>

